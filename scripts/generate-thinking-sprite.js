#!/usr/bin/env node
/* eslint-disable @typescript-eslint/no-require-imports */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SOURCE_HTML = path.join(ROOT, '..', 'claude-logo-draw.html');
const OUTPUT_TS = path.join(ROOT, 'src', 'lib', 'generated-thinking-sprite.ts');

function readFile(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`File not found: ${filePath}`);
  }

  return fs.readFileSync(filePath, 'utf8');
}

function extractMatch(source, regex, label) {
  const match = source.match(regex);
  if (!match) {
    throw new Error(`Unable to extract ${label}`);
  }

  return match[1];
}

function escapeTemplateLiteral(value) {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`');
}

function generateModule({ viewBox, frameCount, defaultFps, pathData }) {
  return `// This file is generated by scripts/generate-thinking-sprite.js
// Source: ../claude-logo-draw.html

export const THINKING_SPRITE_VIEWBOX = '${viewBox}' as const;
export const THINKING_SPRITE_FRAME_COUNT = ${frameCount};
export const THINKING_SPRITE_DEFAULT_FPS = ${defaultFps};
export const THINKING_SPRITE_PATH = \`${escapeTemplateLiteral(pathData)}\`;
`;
}

function main() {
  const html = readFile(SOURCE_HTML);
  const viewBox = extractMatch(
    html,
    /<svg[^>]*id="svg"[^>]*viewBox="([^"]+)"/i,
    'sprite viewBox'
  );
  const pathData = extractMatch(
    html,
    /<svg[^>]*id="svg"[\s\S]*?<path[^>]*d="([^"]+)"/i,
    'sprite path'
  );
  const frameCount = Number(
    extractMatch(html, /const\s+FRAMES\s*=\s*(\d+)\s*;/, 'frame count')
  );
  const defaultFps = Number(
    extractMatch(
      html,
      /<input[^>]*id="speed"[^>]*value="(\d+)"/i,
      'default fps'
    )
  );

  if (!Number.isFinite(frameCount) || frameCount <= 0) {
    throw new Error(`Invalid frame count: ${frameCount}`);
  }

  if (!Number.isFinite(defaultFps) || defaultFps <= 0) {
    throw new Error(`Invalid default fps: ${defaultFps}`);
  }

  fs.writeFileSync(
    OUTPUT_TS,
    generateModule({ viewBox, frameCount, defaultFps, pathData }),
    'utf8'
  );

  console.log(
    `[generate-thinking-sprite] Wrote ${path.relative(ROOT, OUTPUT_TS)} from ${path.relative(ROOT, SOURCE_HTML)}`
  );
}

main();
